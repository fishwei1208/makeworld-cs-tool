function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function filteredFaqs() {
  const term = searchTerm.trim().toLowerCase();
  return faqs.filter((faq) => {
    const inCategory = activeCategory === "all" || faq.category === activeCategory;
    const haystack = `${faq.label} ${faq.title} ${faq.text}`.toLowerCase();
    return inCategory && (!term || haystack.includes(term));
  });
}

function filteredAssets() {
  const term = searchTerm.trim().toLowerCase();
  return assets.filter((asset) => {
    const inCategory = activeCategory === "all" || asset.category === activeCategory;
    const haystack = `${asset.title} ${asset.description}`.toLowerCase();
    return inCategory && (!term || haystack.includes(term));
  });
}

function renderTabs() {
  tabs.innerHTML = Object.entries(categoryNames).map(([key, name]) => `
    <button class="tab ${key === activeCategory ? "active" : ""}" type="button" data-category="${key}">${name}</button>
  `).join("");
}

function renderFaqs() {
  const items = filteredFaqs();
  const imageCount = filteredAssets().length;
  resultCount.textContent = `${categoryNames[activeCategory]}，${items.length} 則回覆 / ${imageCount} 張圖片`;

  if (!items.length) {
    faqGrid.innerHTML = `<div class="empty">沒有找到符合的回覆</div>`;
    return;
  }

  faqGrid.innerHTML = items.map((faq, index) => `
    <article class="faq-card">
      <div class="faq-top">
        <span class="tag">${escapeHtml(faq.label)}</span>
        <button class="icon-button" type="button" data-copy-index="${index}" title="複製回覆" aria-label="複製回覆">
          <span aria-hidden="true">⧉</span>
        </button>
      </div>
      <h3>${escapeHtml(faq.title)}</h3>
      <textarea class="reply-text" data-text-index="${index}">${escapeHtml(faq.text)}</textarea>
      <div class="card-actions">
        <button class="primary-button" type="button" data-copy-index="${index}">複製</button>
        <button class="secondary-button" type="button" data-reset-index="${index}">還原</button>
      </div>
    </article>
  `).join("");
}

